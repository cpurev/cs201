#ifndef BOOL_DIVIS
#define BOOL_DIVIS
/*
	*Find if the number is divisable by 3
	*@param n number to find if its divisible
	*@return boolean value if its possible or not.
*/
bool isDiv3(const int& n);
/*
	*Find if A is divisable by B
	*@param A number is dividend
	*@param B number is divisor
	*@return boolean value if its possible or not.
*/
bool isAdivB(const int& a, const int& b);
#endif // BOOL_DIVIS
